  // Get all provinces
  static List<String> getAllProvinces() {
    return provinces.keys.toList();
  }

  // Get districts by province
  static List<String> getDistricts(String province) {
    return provinces[province]?.districts.map((d) => d.name).toList() ?? [];
  }

  // Get municipalities by province and district
  static List<String> getMunicipalities(String province, String district) {
    return provinces[province]
            ?.districts
            .firstWhere(
              (d) => d.name == district,
              orElse: () => District(name: '', municipalities: []),
            )
            .municipalities
            .toList() ??
        [];
  }

  // Get all municipalities for a province
  static List<String> getAllMunicipalities(String province) {
    final prov = provinces[province];
    if (prov == null) return [];

    List<String> allMunicipalities = [];
    for (var district in prov.districts) {
      allMunicipalities.addAll(district.municipalities);
    }
    return allMunicipalities;
  }
